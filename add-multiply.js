// Add five numbers

function sumOfFiveNumbers(num1, num2, num3, num4, num5){
    const sum = num1 + num2 + num3 + num4 + num5
    return sum;
}

const numbers = sumOfFiveNumbers(45, 54, 54, 54, 456, 76);
console.log(numbers);

// Multiply five numbers 


function multiplyOfFiveNumbers(num1, num2, num3, num4, num5){
    const sum = num1 * num2 * num3 * num4 * num5;
    return sum;
}

const numbers = multiplyOfFiveNumbers(45, 54, 54, 54, 456, 76);
console.log(numbers);
